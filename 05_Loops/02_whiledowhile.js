let index = 0
// while loop entry controlled
while(index<=10) {
    console.log(`Value of index is: ${index}`)
    index = index + 2
}

let myArr = ["flash", "batman", "superman"]
let hero = 0

while(hero<myArr.length) {
    console.log(`Hero Name is: ${myArr[hero]}`)
    hero += 1
}


let score = 1
// do while -> exit controlled
do {
    console.log(`Score is: ${score}`)
    score++
} while(score<=10);